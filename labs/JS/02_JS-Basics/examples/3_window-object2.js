// window - Built in Object

// window.parseInt()
// window.parseFloat()

console.log(parseInt(53)); //53
console.log(parseInt(53.245)); //53
console.log(parseInt("53test")); //53
console.log(parseInt("53.245test")); //53
console.log(parseInt("t53test")); //NaN --> Not a Number

console.log("========================");

console.log(parseFloat(53)); //53
console.log(parseFloat(53.245)); //53.245
console.log(parseFloat("53test")); //53
console.log(parseFloat("53.245test")); //53.245
console.log(parseFloat("t53test")); //NaN --> Not a Number
