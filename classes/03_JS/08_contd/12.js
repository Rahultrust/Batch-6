// Getting date values from date object

const currentDate = new Date();

console.log(currentDate.getFullYear());
console.log(currentDate.getDate()); //day of the month
console.log(currentDate.getDay()); //day of the week 0-6
console.log(currentDate.getHours()); //hours in 24 hr format
console.log(currentDate.getMinutes());
console.log(currentDate.getSeconds());
console.log(currentDate.getMilliseconds());
console.log(currentDate.getTime()); //date time in ms
